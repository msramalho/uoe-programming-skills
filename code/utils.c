#include "utils.h"

/**
 * @brief max between two integers
 * 
 * @param a the first integer
 * @param b the second integer
 * @return int the largest of the passed integers
 */
int max(int a, int b) { return a > b ? a : b; }

/**
 * @brief min between two integers
 * 
 * @param a the first integer
 * @param b the second integer
 * @return int the smallest of the passed integers
 */
int min(int a, int b) { return a < b ? a : b; }